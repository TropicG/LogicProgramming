/*
  Задача 14:
Да се дефинира на пролог предикат p(X), който при даден списък от спицъси X е верен,
ако в X има елемент Y, такъв че X и Y имат единствен общ елемент
*/
member(X, [X | _]).
member(X, [H | T]) :- member(X,T), X \= H.

isCommon(Z,X,Y) :- member(Z,Y), member(Z,X).

% Съществува такъв елемент Y на X, такъв че има елемент Z, който се намира и в X и Y
% но няма друг такъв елемент който принадлежи и на Y и на X.
p(X) :- member(Y,X), isCommon(Z,Y,X), not((isCommon(W), Y, Z), W \= Z).
